public class OperadoresRelacionales {
    public static void main(String[] args) {

        // C)
        float x = 0.005f, y = 0.01f;
        System.out.println("c) " + (x >= 0));

        // D)
        System.out.println("d) " + (x < y--));

        // K)
        System.out.println("k) " + (++x > 0));
    }
}
